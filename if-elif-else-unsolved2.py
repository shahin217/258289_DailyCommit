user_input = input("Enter anything from: lol, rofl, lmk, smh\n")

if user_input == "lol":
    print("laughing out loud")
elif user_input == "rofl":
    print("rolling on the floor laughing")
elif user_input == "lmk":
    print("let me know")
elif user_input == "smh":
    print("shaking my head")
else:
    print("Invalid input\ntry with another one")

